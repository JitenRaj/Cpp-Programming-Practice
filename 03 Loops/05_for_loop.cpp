/* For loop
Increment / Decrement */

#include<iostream>
using namespace std;
int main() {
  // Increament
  for(int i = 0 ; i < 10 ; i++ ) {
    cout<<"For Loop Working : "<< i <<endl;
  }

  // Decrement
  for(int i = 10 ; i > 0 ; i-- ) {
    cout<<"For Loop Working : "<< i <<endl;
  }
  return 0;
}