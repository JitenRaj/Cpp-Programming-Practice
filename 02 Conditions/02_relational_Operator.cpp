#include<iostream>
using namespace std;
int main(){
  if (10==10) // True
  {
    cout<<"10 is equal to 10"<<endl;
  }
  if(4>=2) // True
  {
    cout<<"4 is greater than or equal to 2"<<endl;
  }
  if(5<=6) // True
  {
    cout<<"5 is less than or equal to 6"<<endl;
  }
  if(4!=6) // True
  {
    cout<<"4 is not equal to 6"<<endl;
  }
  return 0;
}