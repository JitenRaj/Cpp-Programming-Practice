#include <iostream>

using namespace std;

int main()
{
    float f = 5.2f;
    double d = 5.59695;

    cout << " Float : " << f << endl;
    cout << " Double : " << d << endl;

   // Creating a double type variable
    double a = 3.912348239293;

    // Creating a float type variable
    float b = 3.912348239293f;

    // Printing the two variables
    cout << "Double Type Number  = " << a << endl;
    cout << "Float Type Number  = " << b << endl;


    return 0;
}
